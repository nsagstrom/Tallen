    public void fillHattList() {
        String query = "SELECT Beskrivning FROM hatt";
        ArrayList<String> hattar = SqlFragor.getEnKolumn(query);
        DefaultListModel model = new DefaultListModel();
        for (String hatt : hattar) {
            model.addElement(hatt);
            JListHattlista.setModel(model);
        }
    }
    

    public void hamtaRitning()
    {
        String hattNamn = JListHattlista.getSelectedValue();
        Runtime rt=Runtime.getRuntime();
        String file="Ritningar/" + hattNamn;
        try {
            Process p=rt.exec("notepad "+file);
        } catch (IOException ex) {
            Logger.getLogger(HamtaHattRitning.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

