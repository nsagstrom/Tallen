    private void fyllRader(ArrayList<HashMap> hattlista){
        Object[][] orderrad = new Object[hattlista.size()][4];
        int rad = 0;
        for(HashMap<String,String> hatt : hattlista){
            orderrad[rad][0] = hatt.get("Beskrivning");
            orderrad[rad][1] = hatt.get("Pris");
            rad++;
        }
        listaRader(orderrad);
    }
    
    /*
    Skriver ut en 2D array i en jList
    */
    private void listaRader(Object[][] orderrad){
        DefaultTableModel model = (DefaultTableModel) tblOrderrader.getModel();
        model.setNumRows(0);
        for (int i=0; i<orderrad.length; i++){
            model.addRow(orderrad[i]);
        }
    }
    
